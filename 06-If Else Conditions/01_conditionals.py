a = 10

# 1. if-elif-else ladder in Python
if(a <= 0):
    print("The value of a is less or equal to 0")
elif(a > 20):
    print("The value of a is greater than 20")
elif(a > 15):
    print("The value of a is greater than 15")
elif(a < 5):
    print("The value of a is less than 5")
else:
    print("The value is between 5 or 15")
